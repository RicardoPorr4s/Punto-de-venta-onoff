    public void mostrarTabla() {
        DefaultTableModel modelo = new DefaultTableModel();
        rs = Conexion.getTabla("select *from cliente");
        //se asignan los nombre de la columna
        modelo.setColumnIdentifiers(new Object[]
        {
            "ID_Cliente", "Nombre", "Apellido paterno", "Apellido materno", "Teléfono","Correo electrónico"});

        try
        {
            while (rs.next())
            {
                modelo.addRow(new Object[]
                {
                    rs.getString("id_cliente"),
                    rs.getString("nombre"),
                    rs.getString("apellido_paterno"),
                    rs.getString("apellido_materno"),
                    rs.getString("telefono"),
                    rs.getString("correo_elec")
                });

            }
            tablaClientes.setModel(modelo);
        } catch (Exception e)
        {
            //JOptionPane.showMessageDialog(null, e);
        }
        //seleccionarTabla();
    }

    public void limpiar() {
        cId.setText("");
        cNombre.setText("");
        cApelliop.setText("");
        cApellidom.setText("");
        cCorreo.setText("");
        cTelefono.setText("");
        tablaClientes.setRowSelectionAllowed(false);
        botonRegistrar.setEnabled(true);
        mostrarTabla();

    }

    private void seleccionarTabla() {
        tablaClientes.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                limpiar();
                int row = tablaClientes.rowAtPoint(e.getPoint());
                cId.setText(tablaClientes.getValueAt(row, 0).toString());
                cNombre.setText(tablaClientes.getValueAt(row, 1).toString());
                cApelliop.setText(tablaClientes.getValueAt(row, 2).toString());
                cApellidom.setText(tablaClientes.getValueAt(row, 3).toString());
                cTelefono.setText(tablaClientes.getValueAt(row, 4).toString());
                cCorreo.setText(tablaClientes.getValueAt(row, 5).toString());
                botonRegistrar.setEnabled(false);

            }
        });

    }

