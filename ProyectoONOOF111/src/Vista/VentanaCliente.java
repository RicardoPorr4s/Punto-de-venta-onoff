    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        // TODO add your handling code here:

        int option = JOptionPane.showConfirmDialog(null, "¿Deseas continuar?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION)
        {
            try
            {
                Connection conexion = Conexion.getConexion();
                ps = conexion.prepareStatement("delete from cliente where id_cliente=?");
                ps.setString(1, cId.getText());
                int resultado = ps.executeUpdate();
                if (resultado > 0)
                {
                    JOptionPane.showMessageDialog(null, "Eliminacion correcta");
                    mostrarTabla();
                } else
                {
                    JOptionPane.showMessageDialog(null, "No se realizo la eliminación de los datos");
                }
                conexion.close();
            } catch (Exception e)
            {
                System.err.println(e);
            }
            limpiar();
            tablaClientes.setRowSelectionAllowed(false);
            botonRegistrar.setEnabled(true);
        } else
        {
            System.out.println("El usuario seleccionó 'No'.");
        }
    }//GEN-LAST:event_bEliminarActionPerformed

    private void botonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonRegistrarActionPerformed
        // TODO add your handling code here:

        int option = JOptionPane.showConfirmDialog(null, "¿Deseas continuar?", "Confirmación", JOptionPane.YES_NO_OPTION);

        if (option == JOptionPane.YES_OPTION)
        {
            Connection conexion = null;
            int resultado = 0;
            boolean yaRegistrado;
            try
            {
                conexion = Conexion.getConexion();
                ps = conexion.prepareStatement("insert into cliente(id_cliente,nombre,apellido_paterno,apellido_materno,telefono, correo_elec)values(?,?,?,?,?,?)");
                ps.setString(1, cId.getText());
                ps.setString(2, cNombre.getText());
                ps.setString(3, cApelliop.getText());
                ps.setString(4, cApellidom.getText());
                ps.setString(5, cTelefono.getText());
                ps.setString(6, cCorreo.getText());

                if(cId.getText().equals("") ||cNombre.getText().equals("") || cApelliop.getText().equals("") ||cApellidom.getText().equals("") 
                        ||cTelefono.getText().equals("")){
                    JOptionPane.showMessageDialog(null, "Rellene todos los campos "); 
                }else if(cCorreo.getText().contains("@") && cCorreo.getText().contains(".")){    
                        resultado = ps.executeUpdate(); 
                    }
                    else {
                        JOptionPane.showMessageDialog(null, "Introduzca un correo válido "); }
                if ((resultado > 0))
                {
                    JOptionPane.showMessageDialog(null, "Datos ingresados correctamente");
                    limpiar();
                    mostrarTabla();
                } else
                {
                    JOptionPane.showMessageDialog(null, "Verifique que los datos sean correctos");

                }
                conexion.close();

            } catch (Exception ex)
            {
                JOptionPane.showMessageDialog(null, "Verifique los datos");
                System.out.println(ex);
            }
            tablaClientes.setRowSelectionAllowed(false);
            botonRegistrar.setEnabled(true);
        } else
        {

        }
    }//GEN-LAST:event_botonRegistrarActionPerformed

