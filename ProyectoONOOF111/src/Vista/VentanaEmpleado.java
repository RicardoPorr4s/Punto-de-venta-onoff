     public void listaEmpleados() {
        listap = new ArrayList<Empleado>();
        listap = (ArrayList) proxy.listaEmpleados();
        pmodelo = (DefaultTableModel) tablaUsuarios.getModel();
        pmodelo.setRowCount(0);
        Object[] ob = new Object[6];
        for (int i = 0; i < listap.size(); i++) {
            ob[0] = listap.get(i).getRFC();
            ob[1] = listap.get(i).getNombre();
            ob[2] = listap.get(i).getApaterno();
            ob[3] = listap.get(i).getAmaterno();
            ob[4] = listap.get(i).getDireccion();
            ob[5] = listap.get(i).getCorreo();
            pmodelo.addRow(ob);
        }
        tablaUsuarios.setModel(pmodelo);
    }

