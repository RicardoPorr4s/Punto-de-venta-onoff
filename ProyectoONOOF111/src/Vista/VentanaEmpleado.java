    private void seleccionarTabla() {
        tablaUsuarios.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent e) {
                limpiar();
                int row = tablaUsuarios.rowAtPoint(e.getPoint());

                cRFC.setText(tablaUsuarios.getValueAt(row, 0).toString());
                cNombre.setText(tablaUsuarios.getValueAt(row, 1).toString());
                cPaterno.setText(tablaUsuarios.getValueAt(row, 2).toString());
                cMaterno.setText(tablaUsuarios.getValueAt(row, 3).toString());
                cDireccion.setText(tablaUsuarios.getValueAt(row, 4).toString());
                cCorreo.setText(tablaUsuarios.getValueAt(row, 5).toString());
                botonRegistrar.setEnabled(false);

                cRFC.setEditable(false);

            }
        });
        cRFC.setEditable(true);

    }

  private void validateEmail() {
        String email = cCorreo.getText().trim();

        // Patr√≥n para validar el formato del correo electr√≥nico
        String emailPattern = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$";

        Pattern pattern = Pattern.compile(emailPattern);
        Matcher matcher = pattern.matcher(email);

        if (matcher.matches()) {
            JOptionPane.showMessageDialog(this, "Correo electr√≥nico v√°lido");
        } else {
            JOptionPane.showMessageDialog(this, "Correo electr√≥nico inv√°lido");
            cCorreo.requestFocus();
        }
    }

